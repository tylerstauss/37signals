$(document).ready(function(){
	  $('#center-left').hover(function(){
   $('#left').toggleClass('hidden');
   $('.hide').toggleClass('hidden');
});
   $('#center-center').hover(function(){
   $('#center').toggleClass('hidden');
   $('.hide').toggleClass('hidden');
});
   $('#center-right').hover(function(){
   $('#right').toggleClass('hidden');
   $('.hide').toggleClass('hidden');
});



});